public class PrimitivesAndClasses {
    public static void main(String args[]) {
        Integer integer1 = 42;
        Integer integer2;
        int int1 = 23;
        int int2;
        integer2 = int1;
        int2 = integer1;
        System.out.println(integer1);
        System.out.println(integer2);
        System.out.println(int1);
        System.out.println(int2);
    }
}
